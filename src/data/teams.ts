// Combined teams export
export const allTeams: Team[] = [...kickballTeams, ...dodgeballTeams];

// Helper functions
export const getTeamsBySport = (
  sportType: "kickball" | "dodgeball"
): Team[] => {
  return allTeams.filter(team => team.sportType === sportType);
};

export const getTeamById = (teamId: string): Team | undefined => {
  return allTeams.find(team => team.id === teamId);
};

export const getPlayerById = (playerId: string): Player | undefined => {
  const allPlayers = [...kickballPlayers, ...dodgeballPlayers];
  return allPlayers.find(player => player.id === playerId);
};

export const getTeamCaptain = (team: Team): Player | undefined => {
  if (!team.captain) return undefined;
  return team.players.find(player => player.id === team.captain);
};
